    left: 50%;
    top: 50%;
    bottom: auto;
    right: auto;
    @include transform(translateX(-50%) translateY(-50%));
  }

  @else if $xy==x {
    left: 50%;
    right: auto;
    @include transform(translateX(-50%));
  }

  @else if $xy==y {
    top: 50%;
    bottom: auto;
    @include transform(translateY(-50%));
  }
}

